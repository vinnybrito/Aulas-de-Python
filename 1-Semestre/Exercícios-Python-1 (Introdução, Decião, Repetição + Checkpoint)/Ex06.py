# 6- Entrar via teclado com o valor da cotação do dólar
# e uma certa quantidade de dólares. Calcular e exibir o valor 
# correspondente em Reais (R$).

c = float(input('Digite o valor da cotação do dólar atual: '))

d = float(input('Digite a quantia em dólar desejada: '))

r = c * d

print('A quantia em dolar convertida para real é: ', r)
